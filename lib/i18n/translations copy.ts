type DeepKeys<T, P extends string = ""> =
  T extends object
    ? {
        [K in keyof T & string]:
          T[K] extends string
            ? `${P}${K}`             // direkter Key, z. B. "passwordForgot"
            : `${P}${K}` | DeepKeys<T[K], `${P}${K}.`>; // verschachtelte Keys
      }[keyof T & string]
    : never;

